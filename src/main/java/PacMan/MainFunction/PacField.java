    /**
     * Draws the Map of Pacman with the designated width and height of Pacman for each Tile
     */
    public void draw(Graphics g) {
        for (int i = 0; i < getMap().length; i++) {
            for (int j = 0; j < getMap()[0].length; j++) {
                if (getMap()[i][j] == 1) {
                    g.setColor(new Color(25, 25, 166));
                    g.drawRect(j * Player.WIDTH, (i * Player.HEIGHT), Player.WIDTH, Player.HEIGHT);
                }else if(getMap()[i][j] == 3){
                    g.setColor(new Color(250, 194, 192));
                    g.fillRect(j * Player.WIDTH, i * Player.HEIGHT, Player.WIDTH-1, Player.HEIGHT/4);
                }
            }
        }
    }

